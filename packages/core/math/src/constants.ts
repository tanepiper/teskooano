/**
 * Mathematical constants used throughout the math package
 */

/** The smallest positive number that can be represented in JavaScript */
export const EPSILON = 0.000001;

/** The mathematical constant π (pi) */
export const PI = Math.PI;

/** Two times π (2π), commonly used in circular calculations */
export const TWO_PI = PI * 2;

/** Half of π (π/2), commonly used in right-angle calculations */
export const HALF_PI = PI / 2;

// Conversion factor from degrees to radians
export const DEG_TO_RAD = PI / 180;
